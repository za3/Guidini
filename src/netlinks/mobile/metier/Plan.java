package netlinks.mobile.metier;

public class Plan {

	public String petitDejeuner;
	public String matinee;
	public String dejeuner;
	public String apresMidi;
	public String diner;
	public String soiree;
	public String date;
	
	public String getPetitDejeuner() {
		return petitDejeuner;
	}
	public void setPetitDejeuner(String petitDejeuner) {
		this.petitDejeuner = petitDejeuner;
	}
	
	public String getMatinee() {
		return matinee;
	}
	public void setMatinee(String matinee) {
		this.matinee = matinee;
	}
	
	public String getDejeuner() {
		return dejeuner;
	}
	public void setDejeuner(String dejeuner) {
		this.dejeuner = dejeuner;
	}
	
	public String getApresMidi() {
		return apresMidi;
	}
	public void setApresMidi(String apresMidi) {
		this.apresMidi = apresMidi;
	}
	
	public String getDiner() {
		return diner;
	}
	public void setDiner(String diner) {
		this.diner = diner;
	}
	
	public String getSoiree() {
		return soiree;
	}
	public void setSoiree(String soiree) {
		this.soiree = soiree;
	}
	
	public String getDate() {
		return date;
	}
	public void setDate(String date) {
		this.date = date;
	}
	
}