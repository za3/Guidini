package netlinks.mobile.metier;

public class Preferences {

	public Integer culture = 1 ;
	public Integer musique = 2 ;
	public Integer shopping = 3 ;
	public Integer cinema = 4 ;
	public Integer religion = 5 ;
	
	public Integer getCulture() {
		return culture;
	}
	public void setCulture(Integer culture) {
		this.culture = culture;
	}
	
	public Integer getMusique() {
		return musique;
	}
	public void setMusique(Integer musique) {
		this.musique = musique;
	}
	
	public Integer getShopping() {
		return shopping;
	}
	public void setShopping(Integer shopping) {
		this.shopping = shopping;
	}
	
	public Integer getCinema() {
		return cinema;
	}
	public void setCinema(Integer cinema) {
		this.cinema = cinema;
	}
	
	public Integer getReligion() {
		return religion;
	}
	public void setReligion(Integer religion) {
		this.religion = religion;
	}
	
}
